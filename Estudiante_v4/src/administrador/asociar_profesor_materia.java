    public void cargarComboBD(String consulta) {

        try {
            Class.forName(drv);
            Conexion = DriverManager.getConnection(url, user, pass);
            SentenciaSQL = Conexion.createStatement();
            Statement SentenciaSQL = Conexion.createStatement();
            ResultSet materias = SentenciaSQL.executeQuery("SELECT materia.codigo_materia,materia.nombre_materia FROM materia WHERE materia.codigo_materia\n"
                    + "    NOT IN (SELECT prof_mat.codigo_materia FROM prof_mat where prof_mat.cedula_profesor='" + consulta + "') ");
            combo_materias.removeAllItems();

            value = new DefaultComboBoxModel();
            combo_materias.setModel(value);

            while (materias.next()) {
                // combo_materias.addItem(materias.getString(2));                
                //System.out.println(materias.getString(2));
                //combo_materias.addItem(new materia(materias.getString(1),materias.getString(2)));
                value.addElement(new materia(materias.getString(1), materias.getString(2)));
            }

            materias.close();
            Conexion.close();
            SentenciaSQL.close();

        } catch (ClassNotFoundException e) {
            System.out.println("Clase no encontrada  ");

        } catch (SQLException e) {
            System.out.println(e);

        }

    }

    
    public void cargarComboProfesor() {

        try {
            Class.forName(drv);
            Conexion = DriverManager.getConnection(url, user, pass);
            SentenciaSQL = Conexion.createStatement();
            Statement SentenciaSQL = Conexion.createStatement();
            ResultSet profesores = SentenciaSQL.executeQuery("select * from profesores");
            combo_profesores.removeAllItems();

            value = new DefaultComboBoxModel();            
            combo_profesores.setModel(value);

            while (profesores.next()) {
                // combo_materias.addItem(materias.getString(2));                
                //System.out.println(materias.getString(2));
                //combo_materias.addItem(new materia(materias.getString(1),materias.getString(2)));
                value.addElement(new profesor(profesores.getString(1), profesores.getString(2),profesores.getString(3)));
            }

          profesores.close();
            Conexion.close();
            SentenciaSQL.close();

        } catch (ClassNotFoundException e) {
            System.out.println("Clase no encontrada  ");

        } catch (SQLException e) {
            System.out.println(e);

        }

    }
    
